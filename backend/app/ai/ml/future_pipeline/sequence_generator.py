class SequenceGenerator:
    """
    Generates sequences from buffered frames.
    """

    def __init__(self):

        pass

    def generate(self, frames):
        """
        Convert buffered landmark frames into
        a sequence for future deep learning models.

        Args:
            frames:
                List of landmark frames.

        Returns:
            Sequence of frames.
        """

        return frames

    def sequence_length(self, sequence):
        """
        Returns the number of frames
        in the generated sequence.
        """

        return len(sequence)